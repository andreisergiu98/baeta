const typedocSidebar = require('./docs/api/typedoc-sidebar.cjs');

function injectTypeDocSidebar(items) {
	return items.map((item) => {
		if (item?.link?.id === 'api/index') {
			return {
				...item,
				label: 'API',
				items: typedocSidebar,
			};
		}
		return item;
	});
}

module.exports = {
	injectTypeDocSidebar,
};

